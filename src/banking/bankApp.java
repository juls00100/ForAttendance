
package banking;

    public class bankApp {
        private int accNo;
        private int pin;
        private float balance;

        public void setAccNo(int accNo) {
            this.accNo = accNo;
        }

        public int getAccNo() {
            return accNo;
        }

        public void setPin(int pin) {
            this.pin = pin;
        }

        public int getPin() {
            return pin;
        }

        public void setBalance(float balance) {
            this.balance = balance;
        }

        public float getBalance() {
            return balance;
        }
    }
    
